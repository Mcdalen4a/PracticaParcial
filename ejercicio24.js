//Escribe una función llamada transformarCadena que reciba una cadena de texto 
//y la devuelva en mayúsculas utilizando toUpperCase(), y luego en minúsculas utilizando toLowerCase().

function transformarCadena(cadena){
    return cadena.toUpperCase()
}

console.log(transformarCadena("hola Como estass"))


function transformarCadena2(cadena2){
    return cadena2.toLowerCase()
}


console.log(transformarCadena2("HOLA COMO ESTAS"))